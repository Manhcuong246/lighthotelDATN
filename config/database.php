        $v = env($key);
        if (filled($v)) {
            return (string) $v;
        }
    }
    foreach (['LARAVEL_DB_PASSWORD', 'DB_PASSWORD'] as $key) {
        $fromFile = $readPasswordFromEnvFile($key);
        if ($fromFile !== null && $fromFile !== '') {
            return $fromFile;
        }
    }

    return '';
})();

